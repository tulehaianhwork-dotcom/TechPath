export function getQuizByLessonId(lessonId: string): Quiz | undefined {
  return quizzes.find(quiz => quiz.lessonId === lessonId);
}

export function calculateXP(score: number, totalQuestions: number): number {
  const percentage = score / totalQuestions;
  if (percentage === 1) return 100; // Perfect score
  if (percentage >= 0.8) return 80;
  if (percentage >= 0.6) return 60;
  return Math.round(percentage * 100);
}
